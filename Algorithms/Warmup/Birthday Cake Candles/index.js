function birthdayCakeCandles(n, ar) {
    // Complete this function
    const max = Math.max.apply(null, ar);

    return ar.reduce((a, x) => {
        if (max == x) a = a + 1;
        return a;
    }, 0);
}

function main() {
    var n = parseInt(readLine());
    ar = readLine().split(' ');
    ar = ar.map(Number);
    var result = birthdayCakeCandles(n, ar);
    process.stdout.write("" + result + "\n");
}

